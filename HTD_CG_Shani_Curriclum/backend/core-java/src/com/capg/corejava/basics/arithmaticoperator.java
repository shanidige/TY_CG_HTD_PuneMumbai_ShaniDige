package com.capg.corejava.basics;

public class arithmaticoperator {
	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		int res = i + j;// addition
		System.out.println(res);
		res = i - j;// substraction
		System.out.println(res);
		res = i * j;// multiplication
		System.out.println(res);
		res = j / i;// divide
		System.out.println(res);
		res = i % j;// modulus
		System.out.println(res);
	}
}

package com.capg.corejava.basics;

public class assignmentoperator {
	public static void main(String[] args) {
		int i = 10;
		int j = 20;
		j+=i;//j=j+i
		System.out.println(j);
		j-=i;
		System.out.println(j);
		j*=i;
		System.out.println(j);
		j/=i;
		System.out.println(j);
		j%=i;
		System.out.println(j);
	}

}
